def 치킨집(종류,개수,돈):
    '''종류를 개수만큼 만들고 돈 받아 거스름돈 주기
치킨집(종류,개수,돈)'''
    menu={'후라이드':4000,'반반':5000,'양념':6000,'맥주':3000}
    print('주문이요!')
    
    for i in range(개수):
        print(종류)
    price=menu[종류]*개수
    print('총가격:',price)
    거스름돈=돈-price
    print('거스름돈:',거스름돈)
    return 거스름돈
#-----------------------

치킨집('후라이드',3,20000)
